def add(num1, num2):
    print(f"{num1+num2}")


def subtract(num1, num2):
    print(f"{num1-num2}")


def multiply(num1, num2):
    print(f"{num1*num2}")


def divide(num1, num2):
    print(f"{num1/num2:.2f}")


a = 50
b = 6
add(a, b)
multiply(a, b)
subtract(a, b)
divide(a, b)

